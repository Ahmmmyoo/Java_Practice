public class ClassWeek2p04
{
    public static void main(String[] args)
    {
        // while loop
        System.out.println("While Loop");
        {
            int x  = 0 ;
            while(x < 101)
            {
            System.out.print(" "+x+" ");
            x++ ;
            }
        }
        // for loop
        System.out.println("");
        System.out.println("For Loop");
        {
            for (int y = 0; y<101;y++)
            {
                System.out.print(" "+y+" ");
            }
        }
    }    
}
